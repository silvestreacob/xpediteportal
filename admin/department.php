function getRow(id){
  $.ajax({
    type: 'POST',
    url: 'department_row.php',
    data: {id:id},
    dataType: 'json',
    success: function(response){
      $('#posid').val(response.id);
      $('#edit_title').val(response.dep_description);
      $('#del_posid').val(response.id);
      $('#del_position').html(response.dep_description);
    }
  });
}
